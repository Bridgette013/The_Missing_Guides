/**
 * sync-products.mjs — products catalog reconciler (Option A)
 *
 * SOURCE OF TRUTH:  The_Missing_Guides/src/config/products.js  (this repo)
 * GENERATED MIRROR: vvv-dIgitals-done/netlify/functions/_lib/products-server.js
 *
 * After the VVV->TMG migration the product catalog is split across two
 * separate Netlify sites with separate builds and no shared filesystem.
 * To prevent two hand-edited copies drifting apart:
 *
 *   - The FULL catalog (src/config/products.js) is canonical and lives HERE.
 *   - VVV's checkout + webhook functions need a trimmed CommonJS mirror.
 *     This script regenerates that mirror and writes it into the VVV copy.
 *   - The generated products-server.js is COMMITTED to the VVV repo.
 *
 * THIS IS A LOCAL DEV STEP — NOT part of any Netlify build.
 * Run by hand after editing src/config/products.js, then commit BOTH repos.
 *
 * Usage:
 *   node scripts/sync-products.mjs
 *   VVV_REPO=/path/to/vvv-dIgitals-done node scripts/sync-products.mjs
 */

import { writeFile } from 'node:fs/promises';
import { existsSync } from 'node:fs';
import { fileURLToPath, pathToFileURL } from 'node:url';
import path from 'node:path';

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const REPO_ROOT = path.join(__dirname, '..');

const SOURCE = path.join(REPO_ROOT, 'src', 'config', 'products.js');

const VVV_REPO =
  process.env.VVV_REPO || path.join(REPO_ROOT, '..', 'vvv-dIgitals-done');

const OUTPUT = path.join(
  VVV_REPO, 'netlify', 'functions', '_lib', 'products-server.js'
);

const SERVER_FIELDS = [
  'slug', 'name', 'tagline', 'priceCents', 'currency',
  'recurring', 'stripeProductName', 'pdfBundle', 'toolAccess', 'status',
];

function pickServerFields(p) {
  const out = {};
  for (const key of SERVER_FIELDS) {
    if (p[key] !== undefined) out[key] = p[key];
  }
  return out;
}

async function main() {
  if (!existsSync(SOURCE)) {
    throw new Error('Canonical catalog not found: ' + SOURCE);
  }

  const outDir = path.dirname(OUTPUT);
  if (!existsSync(outDir)) {
    throw new Error(
      'VVV repo target not found: ' + outDir +
      '. Check out vvv-dIgitals-done as a sibling of this repo, or set ' +
      'VVV_REPO=/absolute/path/to/vvv-dIgitals-done and re-run.'
    );
  }

  const mod = await import(pathToFileURL(SOURCE).href);
  const products = mod.PRODUCTS || mod.products;
  if (!Array.isArray(products)) {
    throw new Error('Expected PRODUCTS array export from ' + SOURCE);
  }

  const trimmed = products.map(pickServerFields);
  const json = JSON.stringify(trimmed, null, 2);

  const header = [
    '/**',
    ' * Server-side mirror of The_Missing_Guides/src/config/products.js',
    ' *',
    ' * AUTO-GENERATED by The_Missing_Guides/scripts/sync-products.mjs.',
    ' * DO NOT EDIT BY HAND. Edit the canonical catalog in the TMG repo',
    ' * (src/config/products.js), re-run the sync script, and commit both.',
    ' *',
    " * VVV's checkout and webhook functions read this CommonJS mirror at",
    ' * runtime. It is committed to the VVV repo (not build-generated).',
    ' */',
  ].join('\n');

  const logic = [
    '',
    'function getProductBySlug(slug) {',
    "  return PRODUCTS.find((p) => p.slug === slug && p.status === 'live');",
    '}',
    '',
    'function getStripeName(product) {',
    '  return product.stripeProductName || product.name;',
    '}',
    '',
    'module.exports = { PRODUCTS, products: PRODUCTS, getProductBySlug, getStripeName };',
    '',
  ].join('\n');

  const body = header + '\n\nconst PRODUCTS = ' + json + ';\n' + logic;

  await writeFile(OUTPUT, body, 'utf8');
  console.log('  Synced ' + trimmed.length + ' product(s)');
  console.log('    source: ' + SOURCE);
  console.log('    mirror: ' + OUTPUT);
  console.log('  Commit BOTH repos (TMG products.js + VVV products-server.js).');
}

main().catch((err) => {
  console.error(err.message || err);
  process.exit(1);
});
